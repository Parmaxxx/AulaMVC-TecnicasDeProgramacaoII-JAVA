    private void btEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEnviarActionPerformed

        String nome = txtNome.getText();
        String login = txtLogin.getText();
        String senha = txtSenha.getText();
        if(Persistencia.getLista().add(new Usuario(nome,login,senha))){
        JOptionPane.showMessageDialog(null, "Usuario Cadastrado");}
        else{
             JOptionPane.showMessageDialog(null, "Erro!","Resposta",JOptionPane.ERROR_MESSAGE);}
                txtNome.setText("");
                txtLogin.setText("");
                txtSenha.setText("");
                
                

        
    }//GEN-LAST:event_btEnviarActionPerformed

    private void btListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btListarActionPerformed
        ArrayList<Usuario> listaCadastrados = new ArrayList();
        listaCadastrados = Persistencia.getLista();
        
        DefaultTableModel tabelamodelo = (DefaultTableModel)tableListar.getModel();
        
        for(Usuario user: listaCadastrados){
            Object[] linha ={user.getNome(),user.getLogin(),user.getSenha()};
            tabelamodelo.addRow(linha);
        }
    }//GEN-LAST:event_btListarActionPerformed

